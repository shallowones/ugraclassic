<p>
	 До настоящего времени неизменным штатным органистом «Югра-Классик» является заслуженный деятель культуры Югры, лауреат международных конкурсов Елена Коземиренко, которая не только получила второе высшее музыкальное образование как органист в Казанской консерватории, но и прошла специальную стажировку в Бонне и Кёльне.
