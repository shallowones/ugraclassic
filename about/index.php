<p>
	 В соответствии с действующим Уставом, целью Концертно-театрального центра «Югра-Классик» является формирование и удовлетворение духовных и нравственных потребностей жителей Ханты-Мансийского автономного округа – Югры через создание и представление произведений музыкального, театрального, чтецкого, хореографического и циркового искусства.
</p>
<p>
	 Ориентиром в деятельности учреждения являются направления государственной политики в области культуры и искусства. При этом особое внимание обращается на повышение доступности и качества услуг в сфере культуры.
