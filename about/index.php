</p>
<p>
	 Общая площадь Большого зала со сценой - 747,5 кв.м. Рабочая глубина сцены - 18,5м, ширина - 15м, диаметр поворотного круга - 11м, зеркало сцены - 7,6х18м.. На сцене установлены 16 электромеханических и 20 ручных штанкетов для крепления и подъема декораций. Самое современное световое и звуковое оборудование зала обеспечивает высокий уровень проводимых мероприятий. Большой зал оборудован профессиональным кинопроектором Ernemann-15 Digital Audio, звуком системы Dolby. Имеется 4 разноформатных проекционных экрана и большой светодиодный экран площадью 60 кв.м.
