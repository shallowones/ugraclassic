 <img alt="КТЦ (2003).jpg" src="/upload/medialibrary/126/1261d95da14a6b7ad4352934472a026d.jpg" title="КТЦ (2003).jpg" hspace="20" height="254" border="0" align="left" width="350" vspace="20">
