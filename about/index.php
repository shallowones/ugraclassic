<p>
	 Орган «Югра-Классик» стал стимулом для расширения международного культурного содружества автономного округа. В рамках абонементов «Органная карта Европы» и «Главные органисты соборов Европы» в Ханты-Мансийске выступили более 60 видных исполнителей из 17 стран.
</p>
