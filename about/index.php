<p>
 <b>Арт-салон</b>&nbsp;– это многофункциональное пространство в классическом «дворцовом» стиле, призванное создать позитивный настрой и комфорт зрителям в преддверии действа и во время антракта. В арт-салоне проходят Рождественские елки, балы, презентации, фуршеты.
</p>
<p>
 <b>Клуб-ресторан «Венское кафе»</b>, помимо ресторанного сервиса высокого класса, предлагает посетителям уютные клубные концерты, клубные встречи, задушевное общение с мастерами искусств. Новинкой последнего сезона стали «квартирники» - неформатные мероприятия в ретро-стиле 60х-90х годов, которые сразу стали пользоваться успехом.
</p>
<p>
 <b>Зал «Амадеус»</b>&nbsp;приобрел новое звучание после проведения в Ханты-Мансийске Саммита «Россия-Евросоюз – 2008». Современная система конференц-связи позволяет проводить здесь пресс-конференции и совещания самого высокого уровня, «круглые столы» и другое.
