<p>
	 В <b>П</b><b>ресс-зале</b> Концертно-театрального центра проводятся пресс-конференции, встречи с журналистами, брифинги. Зал оборудован системой, позволяющей организовать синхронный перевод. В рамках международных фестивалей в зале проходят специальные показы для прессы конкурсной программы фестиваля. Длительное время здесь работал киноклуб «25-й кадр».
</p>
<p>
 <b>Зал-студия «Трансформер»</b>&nbsp;на 80 мест предполагает размещение сцены и зрительских мест с любой стороны. Это позволяет осуществлять новаторские режиссерские замыслы. Помимо камерных спектаклей здесь проводятся клубные встречи, ток-шоу, выставки, репетиции.
