<p>
	 Автономное учреждение Ханты-Мансийского автономного округа – Югры «Концертно-театральный центр «Югра-Классик» было создано по инициативе Правительства автономного округа первоначально как гастрольный центр общероссийского масштаба.
</p>
<p>
	 Миссия «Югра-Классик» – предоставить труженикам Югры, вносящим большой вклад в экономику России, возможность на своей югорской земле приобщаться к высокому искусству, знакомиться с лучшими исполнителями окружного, российского и мирового уровня. Концепция развития Концертно-театрального центра направлена, в целом, на интеграцию Югры в единое культурное пространство России.
</p>
<p>
	 Архитектурный проект концертно-театрального комплекса, разработанный группой московских архитекторов под руководством Светланы Пейд, был отмечен золотым дипломом фестиваля «Зодчество» (Москва) и серебряной медалью на международном конкурсе архитекторов в Болгарии.
</p>
<p>
	 Многофункциональный комплекс Концертно-театрального центра «Югра-Классик» был торжественно открыт в День образования Ханты-Мансийского автономного округа – 10 декабря 2004 года.
</p>
