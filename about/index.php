 На торжественной церемонии открытия органа 29 февраля 2008 года присутствовали Губернатор Югры, глава фирмы «Иоганес Клайс Оргельбау», видные органисты из Германии и России. Все они подтвердили высокий уровень механических и акустических качеств органа и охарактеризовали его как одного из лучших камерных органов.&nbsp;<br>
