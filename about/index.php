<p>
	 Общая площадь комплекса – 20 тыс. кв. метров. Комплекс включает 4 зрительных зала (Большой, Органный, зал-Трансформер и пресс-зал), конференц-зал «Амадеус», арт-салон, фойе «Свет оникса», клуб-ресторан «Венское кафе», гостиницу на 30 номеров (включая 1 «люкс» и 2 «полу-люкса»), зимнюю парковку на 50 мест, артистические, офисные и технические помещения.
</p>
