<p>
 <u>О Летней творческой школе</u>
</p>
<p>
	 Этот пролонгированный проект, получивший название «Мир через культуру», был создан в целях оказания поддержки одаренной югорской молодежи в ее дальнейшем развитии в рамках творческого содружества Концертно-театрального центра с мастерами искусств России.
</p>
<p>
	 В период работы ежегодной Летней творческой школы (ЛТШ) «Мир через культуру» проводились разнообразные мастер-классы по таким направлениям, как актерское мастерство, хореография, симфоническое и камерное музицирование, вокал, арт-критика, арт-менеджмент, маркетинг. По итогам подготовки по программам Школы было создано более 30 концертных и театральных проектов, в которых принимали участие как слушатели Школы, так и сами мастера искусств.
</p>
<p>
	 Летняя творческая школа успешно работала с 2001 по 2012 годы. Программа Школы ежегодно меняла свое направление, решая актуальные задачи:
</p>
<p>
	 • 2001 год – воспитание активного зрителя и слушателя;
</p>
<p>
	 • 2002 год – повышение профессионализма исполнителей округа, создание пространства арт-менеджмента и арт-критики;
</p>
<p>
	 • 2003 год – объединение коллективов Ханты-мансийского округа и известных музыкантов России в творческом проекте «Музыка пространств»;
</p>
<p>
	 • 2004 год – создание окружных творческих коллективов «Большой джаз Югры», «Югорский трамплин», «Мастер»;
</p>
<p>
	 • 2005 год – создание окружных профессиональных исполнительских коллективов, презентация проектов предстоящего сезона, посвященного 75-летию Югры;
</p>
<p>
	 • 2006 год – развитие партнерских связей югорских артистов с ведущими деятелями культуры и искусства российского и мирового сообщества;
</p>
<p>
	 • 2007 год – создание профессиональных конкурентоспособных шоу-программ в области популярной музыки, продвижение молодых эстрадных исполнителей Югры;
</p>
<p>
	 • 2008 год – приобщение юных музыкантов Югры, Урала и Западной Сибири к искусству симфонического музицирования Маэстро Юрия Темирканова;
</p>
<p>
	 • 2009-2010 гг. – формирование единого исполнительского пространства детских коллективов, создание премьерных проектов творческого сезона 2009 – 2010 г.;
</p>
<p>
	 • 2011 год – создание творческих премьерных проектов к Культурной Олимпиаде «2012 - Год Музыки»;
</p>
<p>
	 • 2012 год – Школа повышения профессионального мастерства югорских артистов с участием солистов и педагогов Мариинского театра.
