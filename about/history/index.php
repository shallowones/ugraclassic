?><p>
	 Автономное (до октября 2014 года – бюджетное) учреждение Ханты-Мансийского автономного округа – Югры «Концертно-театральный центр «Югра-Классик» было создано в 2001 году по инициативе Правительства автономного округа первоначально как Гастрольно-концертное агентство. Позднее оно было переименовано в Концертно-театральный центр, который приобрел свое брендовое имя «Югра-Классик».
</p>
<p>
	 Первые годы учреждение не имело своего здания и располагалось в арендованных помещениях окружного Киновидеоцентра, а культурные мероприятия проводились в залах музея Игошева, гостиничного комплекса «Югорская долина» и в Центре искусств для одаренных детей Севера. В летний период, начиная с 2001 года, стала работать творческая школа, объединившая югорскую творческую молодежь и видных мастеров искусств России. На речных теплоходах, курсирующих по маршрутам Москва - Санкт-Петербург и по Золотому кольцу, проводились мастер-классы и совместные концерты югорчан и мастеров искусств. С 2005 года маршруты таких «творческих» теплоходов пролегли по рекам Югры.
</p>
<p>
	 Новое здание Концертно-театрального центра было спроектировано группой московских архитекторов как многофункциональный комплекс. В 2002 году в центре Ханты-Мансийска была забита первая свая, а затем в рекордно короткие сроки построено и сдано в эксплуатацию современное трехэтажное здание в стиле «hi-tech», оснащенное новейшим оборудованием. Его торжественное открытие было приурочено ко Дню образования Ханты-Мансийского автономного округа 10 декабря 2004 года.
</p>
<p>
	 С этого времени культурная жизнь в автономном округе стала консолидироваться вокруг Концертно-театрального центра «Югра-Классик». Одной из важных функций Концертно-театрального центра стало вовлечение в культурную жизнь городов и поселков региона. На культурное воссоединение направлены такие акции «Югра-Классик», как выездные концерты, спектакли и мастер-классы.
</p>
<p>
	 Начав с организации в Югре гастрольных концертов мастеров искусств российского и мирового уровня, «Югра-Классик» со временем стал создавать и развивать свои собственные профессиональные творческие коллективы, которые формировались, в основном, из выпускников ханты-мансийских филиалов Московской государственной академии музыки им.Гнесиных и МГУКИ (Московского государственного университета культуры и искусства).
</p>
<p>
	 В январе 2016 года, в результате реорганизации ряда окружных учреждений культуры, в состав Концертно-театрального центра «Югра-Классик» вошли новые творческие коллективы: Духовой оркестр Югры, Ансамбль песни и танца, Ансамбль русской песни «Млада» и Международный центр моды.
</p>
<p>
	 В настоящее время штатная численность объединенного коллектива «Югра-Классик» составляет более 200 человек.
</p>
